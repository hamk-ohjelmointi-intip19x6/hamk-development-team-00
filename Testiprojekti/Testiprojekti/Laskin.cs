using System;
namespace Testiprojekti
{
    public class Laskin
    {
        public Laskin()
        {
        }

        public int Summa(int luku1, int luku2)
        {
            return luku1 + luku2;
        }

        public int Erotus(int luku1, int luku2)
        {
            return luku1 - luku2;
        }

        public int Tulo(int v1, int v2)
        {
            return v1 * v2;
        }
    }
}
